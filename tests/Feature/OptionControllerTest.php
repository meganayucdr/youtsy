            return;
        }
        $response->assertViewIs('options.edit');
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @return void
     */
    public function testUpdate()
    {
        if (!Route::has('options.update')) { $this->expectNotToPerformAssertions(); return; }
        $user = factory(User::class)->create();

        $this->actingAs($user);

        $option = factory(Option::class)->create();

        $response = $this->put(route('options.update', [ $option->getKey() ]), factory(Option::class)->make()->toArray());
